from wagtail.blocks import CharBlock, ChoiceBlock

from foundation_cms.base.models.base_block import BaseBlock


class FruElementBlock(BaseBlock):

    ELEMENT_TYPES = [
        ("donation-form", "Donation Form"),
        ("donor-map", "Donor Map"),
        ("top-locations", "Top Locations"),
        ("donation-levels", "Donation Levels"),
    ]

    fru_element_type = ChoiceBlock(
        choices=ELEMENT_TYPES, required=True, help_text="Select the type of Fundraise Up element."
    )

    fru_element_embed_code = CharBlock(
        required=True,
        help_text="Copy the HTML code generated by the desired Fundraise Up element and paste it here.",
    )

    class Meta:
        icon = "form"
        label = "Fundraise Up Element"
        template_name = "fru_element_block.html"
